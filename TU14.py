Mystring = "hello world"
print(Mystring)

print(Mystring[0:3])
#This prints out the characters from 0 to 3 not inclusive. So it will only print 0,1,2

print(Mystring[3::])
#This prints from character 3 to the end. Used when you don't know the length of the string

print(Mystring[0:6:2])
#First number is where it starts from. So Char 0. Second number denotes how far it will go. Last number is the jump it takes.

print(Mystring[-1:-7:-1])
#Prints the string backwards

print(Mystring[0:-2])
#This prints the whole thing except the last 2 chars

